#defining operations for calculator

def addition(a, b):
    return a + b

def subtraction(a, b):
    return a - b

def division(a, b):
    return a / b

def multiplication(a, b):
    return a * b

def percent(a, b):
    return (a*b)/100

def potentiation(a, b):
    return a ** b

def radiciation(a):
    return a ** 0.5

